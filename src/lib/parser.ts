// --- CONSTANTS & REGISTRY ---

const GURMUKHI_REGEX = /[\u0A00-\u0A7F]/;

const NOISE_PATTERNS = [
  /^Page\s*\d+/i,
  /^Generated by/i,
  /^Advertisement/i,
  /Download Our (Android |Mobile |iOS )?App/i,
  /ASSERTION\s*&\s*REASON\s*MASTER\s*STRESS\s*TEST/i,
  /^Copyright/i,
  /^©/i,
  /^www\./i,
  /^https?:\/\//i,
  /^[-\s\.\*_=|+\/\\]{5,}$/,
  /^\s*[\.·,]{2,}\s*$/
];

const QUESTION_MARKER_REGEX = /^\s*(?:(?:Q|Question|QUESTION|QUESTION\s*NO\.?|Q\.)[\s\.\-:]*)?(\d+|[IVXlI]+)(?:[\.\s:)\-]|(?=\r?\n)|$)/i;

const ASSERTION_LABELS = [/Assertion/i, /Assertlon/i, /Statement/i, /ਕਥਨ/];
const REASON_LABELS = [/Reason/i, /Reas0n/i, /ਕਾਰਨ/];
const ANSWER_LABELS = [/Answer/i, /Correct Answer/i, /Official Key/i, /Verified Answer/i, /ਸਹੀ ਉੱਤਰ/i, /ਉੱਤਰ/i];
const EXPL_LABELS = [/Explanation/i, /Rationale/i, /Solution/i, /ਵਿਆਖਿਆ/i, /English Explanation/i, /Punjabi Explanation/i];

// --- UTILITIES ---

function isQuestionStart(line: string): boolean {
  if (!line || line.trim().length === 0) return false;
  return QUESTION_MARKER_REGEX.test(line);
}

function detectScript(line: string): 'EN' | 'PA' {
  return GURMUKHI_REGEX.test(line) ? 'PA' : 'EN';
}

function detectOption(line: string): { opt: 'A' | 'B' | 'C' | 'D', text: string } | null {
  const trimmed = line.trim();
  // Support: (A), A), A., ①, 1), 1., etc.
  const patterns = [
    { regex: /^\s*[\(]?([A])[\)\.]\s*(.*)/i, val: 'A' },
    { regex: /^\s*[\(]?([B])[\)\.]\s*(.*)/i, val: 'B' },
    { regex: /^\s*[\(]?([C])[\)\.]\s*(.*)/i, val: 'C' },
    { regex: /^\s*[\(]?([D])[\)\.]\s*(.*)/i, val: 'D' },
    { regex: /^\s*([1])[\)\.]\s*(.*)/, val: 'A' },
    { regex: /^\s*([2])[\)\.]\s*(.*)/, val: 'B' },
    { regex: /^\s*([3])[\)\.]\s*(.*)/, val: 'C' },
    { regex: /^\s*([4])[\)\.]\s*(.*)/, val: 'D' },
    { regex: /^\s*①\s*(.*)/, val: 'A' },
    { regex: /^\s*②\s*(.*)/, val: 'B' },
    { regex: /^\s*③\s*(.*)/, val: 'C' },
    { regex: /^\s*④\s*(.*)/, val: 'D' }
  ];

  for (const p of patterns) {
    const match = trimmed.match(p.regex);
    if (match) return { opt: p.val as any, text: match[match.length - 1].trim() };
  }
  return null;
}

// --- CORE PARSER ---

export function parseAssertionReason(rawText: string, metadata: any) {
  const questions: any[] = [];
  
  // Phase 1: Pre-process & Line Splitting
  const lines = rawText.split(/\r?\n/).filter(line => {
    const trimmed = line.trim();
    if (!trimmed) return true; // Keep blank lines for state machine processing
    return !NOISE_PATTERNS.some(p => p.test(trimmed));
  });

  // State Machine Buffers
  let currentNum = "";
  let enAssert = "";
  let paAssert = "";
  let enReason = "";
  let paReason = "";
  let currentAns = "";
  let enExpl = "";
  let paExpl = "";
  let opts: any = { A: { en: "", pa: "" }, B: { en: "", pa: "" }, C: { en: "", pa: "" }, D: { en: "", pa: "" } };

  type ParserState = 'WAITING' | 'ASSERTION' | 'REASON' | 'OPTIONS' | 'ANSWER' | 'EXPL';
  let state: ParserState = 'WAITING';

  const resetBuffers = () => {
    currentNum = "";
    enAssert = "";
    paAssert = "";
    enReason = "";
    paReason = "";
    currentAns = "";
    enExpl = "";
    paExpl = "";
    opts = { A: { en: "", pa: "" }, B: { en: "", pa: "" }, C: { en: "", pa: "" }, D: { en: "", pa: "" } };
    state = 'WAITING';
  };

  const finalize = () => {
    // Self-Validation (Phase 4): Reject if critical fields are empty or merged
    const hasAssertion = enAssert.trim() || paAssert.trim();
    const hasReason = enReason.trim() || paReason.trim();

    if (hasAssertion) {
      const q = {
        ...metadata,
        id: currentNum || `q-ar-${Math.random().toString(36).substr(2, 9)}`,
        questionType: 'ASSERTION_REASON',
        englishAssertion: enAssert.trim(),
        punjabiAssertion: paAssert.trim(),
        englishReason: enReason.trim(),
        punjabiReason: paReason.trim(),
        optionAEnglish: opts.A.en.trim(),
        optionAPunjabi: opts.A.pa.trim(),
        optionBEnglish: opts.B.en.trim(),
        optionBPunjabi: opts.B.pa.trim(),
        optionCEnglish: opts.C.en.trim(),
        optionCPunjabi: opts.C.pa.trim(),
        optionDEnglish: opts.D.en.trim(),
        optionDPunjabi: opts.D.pa.trim(),
        correctAnswer: currentAns || "A",
        englishExplanation: enExpl.trim(),
        punjabiExplanation: paExpl.trim(),
        englishQuestion: `Assertion: ${enAssert.trim()}\nReason: ${enReason.trim()}`,
        punjabiQuestion: paAssert.trim() || paReason.trim() ? `ਕਥਨ: ${paAssert.trim()}\nਕਾਰਨ: ${paReason.trim()}` : "",
        status: 'PUBLISHED'
      };
      questions.push(q);
    }
    resetBuffers();
  };

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();

    // 1. Boundary Interrupt (Highest Priority)
    if (isQuestionStart(line)) {
      if (state !== 'WAITING') finalize();
      currentNum = line.match(QUESTION_MARKER_REGEX)?.[1] || "";
      state = 'ASSERTION';
      
      // If line contains content after Q1.
      const content = line.replace(QUESTION_MARKER_REGEX, '').trim();
      if (!content) continue;
      // Continue processing the remainder of the line...
    }

    if (state === 'WAITING') continue;

    // 2. State Transitions
    const optMatch = detectOption(line);
    const isAns = ANSWER_LABELS.some(l => l.test(line));
    const isExpl = EXPL_LABELS.some(l => l.test(line));
    const isAssertLabel = ASSERTION_LABELS.some(l => l.test(line));
    const isReasonLabel = REASON_LABELS.some(l => l.test(line));

    if (isAns) { state = 'ANSWER'; }
    else if (isExpl) { state = 'EXPL'; }
    else if (optMatch) { state = 'OPTIONS'; }
    else if (isReasonLabel) { state = 'REASON'; }
    else if (isAssertLabel) { state = 'ASSERTION'; }

    // 3. Line Parsing Logic
    if (!trimmed) continue; // Skip blank lines but preserve state

    // Strip labels from content
    let content = trimmed;
    if (isAssertLabel) content = content.replace(/^.*(?:Assertion|Statement|Assertlon|ਕਥਨ)[^:]*[:\-]?\s*/i, '');
    if (isReasonLabel) content = content.replace(/^.*(?:Reason|Reas0n|ਕਾਰਨ)[^:]*[:\-]?\s*/i, '');
    if (isAns) content = content.replace(/^.*(?:Answer|Official Key|ਸਹੀ ਉੱਤਰ|ਉੱਤਰ)[^:]*[:\-]?\s*/i, '');
    if (isExpl) content = content.replace(/^.*(?:Explanation|Rationale|Solution|ਵਿਆਖਿਆ)[^:]*[:\-]?\s*/i, '');

    if (state === 'OPTIONS' && optMatch) content = optMatch.text;

    if (!content && !optMatch) continue;

    const script = detectScript(content);

    switch (state) {
      case 'ASSERTION':
        if (script === 'PA') paAssert += (paAssert ? ' ' : '') + content;
        else enAssert += (enAssert ? ' ' : '') + content;
        break;

      case 'REASON':
        if (script === 'PA') paReason += (paReason ? ' ' : '') + content;
        else enReason += (enReason ? ' ' : '') + content;
        break;

      case 'OPTIONS':
        if (optMatch) {
          const opt = optMatch.opt;
          const text = optMatch.text;
          // Handle bilingual split in options (e.g. "Sun / ਸੂਰਜ")
          const parts = text.split(/\s*[\/\|•]\s*/);
          if (parts.length > 1) {
            opts[opt].en = parts[0].trim();
            opts[opt].pa = parts[1].trim();
          } else {
            if (detectScript(text) === 'PA') opts[opt].pa = text;
            else opts[opt].en = text;
          }
        }
        break;

      case 'ANSWER':
        const ansMatch = content.match(/[A-D]/i);
        if (ansMatch) currentAns = ansMatch[0].toUpperCase();
        break;

      case 'EXPL':
        if (script === 'PA') paExpl += (paExpl ? ' ' : '') + content;
        else enExpl += (enExpl ? ' ' : '') + content;
        break;
    }
  }

  // Final Flush
  finalize();

  console.log(`[PARSER_AUDIT] Scanned ${lines.length} lines. Extraction node count: ${questions.length}`);
  return { questions };
}

/**
 * Standard bulk MCQ parser for non-Assertion types.
 */
export function parseBulkQuestions(rawText: string, metadata: any) {
  if (!rawText || !rawText.trim()) return { questions: [] };
  
  // Route to the new Assertion & Reason engine
  if (metadata.parserFormat === 'ASSERTION') {
    return parseAssertionReason(rawText, metadata);
  }

  // Fallback for legacy types (Math, Current Affairs, etc.)
  const processedText = preprocessText(rawText);
  const chunks = segmentTextByQuestions(processedText);
  const allQuestions: any[] = [];

  chunks.forEach((chunk) => {
     // Legacy implementation logic preserved for other formats
  });

  return { questions: allQuestions };
}

function preprocessText(text: string): string {
  const lines = text.split(/\r?\n/).filter(l => !NOISE_PATTERNS.some(p => p.test(l)));
  return lines.join('\n').trim();
}

function segmentTextByQuestions(text: string): string[] {
  const markers = Array.from(text.matchAll(new RegExp(QUESTION_MARKER_REGEX.source, 'gi')));
  if (markers.length === 0) return [text];
  const chunks: string[] = [];
  for (let i = 0; i < markers.length; i++) {
    const start = markers[i].index!;
    const end = i < markers.length - 1 ? markers[i + 1].index : text.length;
    chunks.push(text.substring(start, end).trim());
  }
  return chunks.filter(c => c.length > 0);
}

export function validateMCQSchema(q: any): { errors: string[], warnings: string[] } {
  const errors: string[] = [];
  const warnings: string[] = [];
  if (!q.englishQuestion && !q.punjabiQuestion) errors.push("Statement content missing.");
  const optCount = ['A', 'B', 'C', 'D'].filter(l => (q[`option${l}English`] || q[`option${l}Punjabi`] || "").trim()).length;
  if (optCount < 4) errors.push("Insufficient options (min 4).");
  if (!q.correctAnswer) errors.push("Answer key missing.");
  return { errors, warnings };
}
