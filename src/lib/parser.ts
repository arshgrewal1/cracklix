// --- CONSTANT REGISTRY ---
export const QUESTION_SENTINEL_REGEX = /(?:\r?\n|^)\s*(?:(?:Q|Question|QUESTION|Q\.|Q\s|Question\s|QUESTION\s|QUESTION\sNO\.?|Question\sNO\.?)\s*)?(\d+|[lI])(?:[\.\s:)\-]|(?=\r?\n)|$)/i;

export const NOISE_PATTERNS = [
  /^Page\s*\d+/i,
  /^Generated by/i,
  /^Advertisement/i,
  /Download Our (Android |Mobile |iOS )?App/i,
  /^Download/i,
  /^www\./i,
  /^https?:\/\//i,
  /^Copyright/i,
  /^©/i,
  /@/,
  /^\s*(?:Question|QUESTION|Q\.)\s*$/i,
  /^[-\s\.\*_=|+\/\\]{3,}$/,
  /^\s*[\.·,]{1,}\s*$/
];

export const ANS_MARKERS = ["Answer", "Official Key", "Correct Answer", "ਉੱਤਰ", "उत्तर", "ਸਹੀ ਉੱਤਰ", "Sahi Uttar"];
export const EXPL_MARKERS = ["Explanation", "Solution", "ਵਿਆਖਿਆ", "व्याख्या", "Rationale", "ENGLISH EXPLANATION", "PUNJABI EXPLANATION"];
export const INSTR_KEYWORDS = [
  "Choose the correct matching", 
  "Select the correct answer", 
  "Choose the correct option", 
  "Match correctly", 
  "Pick the correct combination", 
  "ਸਹੀ ਮਿਲਾਨ ਦੀ ਚੋਣ ਕਰੋ", 
  "ਸਹੀ ਉੱਤਰ ਚੁਣੋ",
  "Which option shows the correct matching?",
  "Which of the following is correctly matched?",
  "Identify the correct matching.",
  "Find the correct matching.",
  "Select the correctly matched option."
];
export const ACTUAL_QUESTION_KEYWORDS = /^\s*(Which|What|Who|How|Where|When|Identify|Choose|Find|Observe|Count|Select|During\s?which|How\s?many|How\s?much)\s/i;

const GURMUKHI_REGEX = /[\u0A00-\u0A7F]/;

// --- UTILITIES ---

export function isQuestionStart(line: string): boolean {
  if (!line) return false;
  return QUESTION_SENTINEL_REGEX.test(line);
}

function segmentTextByQuestions(text: string): string[] {
  const markerRegex = /(?:\r?\n|^)\s*(?:(?:Q|Question|QUESTION|Q\.|Q\s|Question\s|QUESTION\s|QUESTION\sNO\.?|Question\sNO\.?)\s*)?(\d+|[lI])(?:[\.\s:)\-]|(?=\r?\n)|$)/gi;
  const matches = Array.from(text.matchAll(markerRegex));
  
  if (matches.length === 0) return [text];

  const chunks: string[] = [];
  for (let i = 0; i < matches.length; i++) {
    const start = matches[i].index!;
    const end = i < matches.length - 1 ? matches[i + 1].index : text.length;
    chunks.push(text.substring(start, end).trim());
  }
  return chunks.filter(c => c.length > 0);
}

export function preprocessText(text: string): string {
  if (!text) return "";
  const lines = text.split(/\r?\n/);
  const cleanLines = lines.filter(line => {
    const trimmed = line.trim();
    if (!trimmed) return true; 
    const isNoise = NOISE_PATTERNS.some(pattern => pattern.test(trimmed));
    return !isNoise;
  });
  return cleanLines.join('\n').trim();
}

export function detectOptionMarker(line: string): { opt: 'A' | 'B' | 'C' | 'D', text: string } | null {
  const trimmed = line.trim();
  const patterns = [
    { regex: /^\s*[\(]?([A])[\)\.]\s*(.*)/i, val: 'A' },
    { regex: /^\s*[\(]?([B])[\)\.]\s*(.*)/i, val: 'B' },
    { regex: /^\s*[\(]?([C])[\)\.]\s*(.*)/i, val: 'C' },
    { regex: /^\s*[\(]?([D])[\)\.]\s*(.*)/i, val: 'D' },
    { regex: /^\s*[\(]?(1)[\)\.]\s*(.*)/, val: 'A' },
    { regex: /^\s*[\(]?(2)[\)\.]\s*(.*)/, val: 'B' },
    { regex: /^\s*[\(]?(3)[\)\.]\s*(.*)/, val: 'C' },
    { regex: /^\s*[\(]?(4)[\)\.]\s*(.*)/, val: 'D' },
    { regex: /^\s*([①❶])\s*(.*)/, val: 'A' },
    { regex: /^\s*([②❷])\s*(.*)/, val: 'B' },
    { regex: /^\s*([③❸])\s*(.*)/, val: 'C' },
    { regex: /^\s*([④❹])\s*(.*)/, val: 'D' }
  ];

  for (const p of patterns) {
    const match = trimmed.match(p.regex);
    if (match) return { opt: p.val as any, text: match[match.length - 1].trim() };
  }
  return null;
}

// --- REDESIGNED ASSERTION & REASON SCANNER ---

export function parseAssertionReason(fullText: string, metadata: any) {
  const lines = fullText.split(/\r?\n/);
  const questions: any[] = [];
  
  let currentQ: any = null;
  let state: 'IDLE' | 'ASSERT_EN' | 'ASSERT_PA' | 'REASON_EN' | 'REASON_PA' | 'OPTIONS' | 'ANSWER' | 'EXPL' = 'IDLE';

  const finalize = () => {
    if (currentQ) {
      const hasContent = (currentQ.assertion.english || currentQ.assertion.punjabi) && 
                         (currentQ.reason.english || currentQ.reason.punjabi);
      if (hasContent) {
        currentQ.englishQuestion = currentQ.assertion.english || "Assertion & Reason Question";
        currentQ.punjabiQuestion = currentQ.assertion.punjabi || "";
        questions.push(currentQ);
      }
    }
  };

  const assertEnRegex = /^\s*(?:Assertion|Assertlon|Assertion:|Assertion\s*\(A\)|Assertion\(A\)|A\)|Statement|Statement\s*\(A\)|Assertion\s*-)\s*[:\-]?/i;
  const assertPaRegex = /^\s*(?:ਕਥਨ|ਕਥਨ:|ਕਥਨ\s*\(A\))\s*[:\-]?/;
  const reasonEnRegex = /^\s*(?:Reason|Reas0n|Reason:|Reason\s*\(R\)|Reason\(R\)|R\)|Reason\s*-)\s*[:\-]?/i;
  const reasonPaRegex = /^\s*(?:ਕਾਰਨ|ਕਾਰਨ:|ਕਾਰਨ\s*\(R\))\s*[:\-]?/;
  const ansRegex = /^\s*(?:Answer|Correct Answer|Official Key|ਉੱਤਰ|उत्तर|ਸਹੀ ਉੱਤਰ|Sahi Uttar)\s*[:\-]?/i;
  const explRegex = /^\s*(?:Explanation|Solution|ਵਿਆਖਿਆ|व्याख्या|Rationale|ENGLISH EXPLANATION|PUNJABI EXPLANATION)\s*[:\-]?/i;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();
    if (!trimmed) continue;

    // Boundary: New Question
    if (isQuestionStart(line)) {
      finalize();
      currentQ = {
        ...metadata,
        id: `q-ar-${Math.random().toString(36).substr(2, 9)}`,
        questionType: 'ASSERTION_REASON',
        assertion: { english: "", punjabi: "" },
        reason: { english: "", punjabi: "" },
        englishQuestion: "", 
        punjabiQuestion: "",
        correctAnswer: "",
        englishExplanation: "",
        punjabiExplanation: "",
        status: 'PUBLISHED',
        createdAt: serverTimestamp(),
        updatedAt: serverTimestamp()
      };
      state = 'IDLE';
      continue;
    }

    if (!currentQ) continue;

    // Metadata Terminate
    if (NOISE_PATTERNS.some(p => p.test(trimmed)) && state === 'EXPL') {
      finalize();
      currentQ = null;
      continue;
    }

    const optMarker = detectOptionMarker(line);
    
    // State Transitions with Script Heuristics
    if (assertEnRegex.test(trimmed)) { state = 'ASSERT_EN'; }
    else if (assertPaRegex.test(trimmed)) { state = 'ASSERT_PA'; }
    else if (reasonEnRegex.test(trimmed)) { state = 'REASON_EN'; }
    else if (reasonPaRegex.test(trimmed)) { state = 'REASON_PA'; }
    else if (ansRegex.test(trimmed)) { state = 'ANSWER'; }
    else if (explRegex.test(trimmed)) { state = 'EXPL'; }
    else if (optMarker && (state === 'REASON_PA' || state === 'REASON_EN' || state === 'OPTIONS')) { state = 'OPTIONS'; }
    else {
      // Continuation heuristic: If we see Gurmukhi in Assert EN mode, it's actually Assert PA
      if (state === 'ASSERT_EN' && GURMUKHI_REGEX.test(trimmed)) state = 'ASSERT_PA';
      if (state === 'REASON_EN' && GURMUKHI_REGEX.test(trimmed)) state = 'REASON_PA';
    }

    // Content Extraction
    let content = trimmed;
    if (assertEnRegex.test(trimmed)) content = trimmed.replace(assertEnRegex, '').trim();
    else if (assertPaRegex.test(trimmed)) content = trimmed.replace(assertPaRegex, '').trim();
    else if (reasonEnRegex.test(trimmed)) content = trimmed.replace(reasonEnRegex, '').trim();
    else if (reasonPaRegex.test(trimmed)) content = trimmed.replace(reasonPaRegex, '').trim();

    if (!content && (assertEnRegex.test(trimmed) || assertPaRegex.test(trimmed) || reasonEnRegex.test(trimmed) || reasonPaRegex.test(trimmed))) continue;

    switch (state) {
      case 'ASSERT_EN':
        currentQ.assertion.english += (currentQ.assertion.english ? '\n' : '') + content;
        break;
      case 'ASSERT_PA':
        currentQ.assertion.punjabi += (currentQ.assertion.punjabi ? '\n' : '') + content;
        break;
      case 'REASON_EN':
        currentQ.reason.english += (currentQ.reason.english ? '\n' : '') + content;
        break;
      case 'REASON_PA':
        currentQ.reason.punjabi += (currentQ.reason.punjabi ? '\n' : '') + content;
        break;
      case 'OPTIONS':
        if (optMarker) {
          currentQ[`option${optMarker.opt}English`] = optMarker.text;
        }
        break;
      case 'ANSWER':
        const ansMatch = trimmed.match(/[A-D]/i);
        if (ansMatch) currentQ.correctAnswer = ansMatch[0].toUpperCase();
        break;
      case 'EXPL':
        const cleanExpl = trimmed.replace(explRegex, '').replace(ansRegex, '').trim();
        if (cleanExpl) {
          if (GURMUKHI_REGEX.test(cleanExpl)) {
            currentQ.punjabiExplanation += (currentQ.punjabiExplanation ? '\n' : '') + cleanExpl;
          } else {
            currentQ.englishExplanation += (currentQ.englishExplanation ? '\n' : '') + cleanExpl;
          }
        }
        break;
    }
  }

  finalize();
  return { questions };
}

// --- REMAINDER OF PARSERS ---

export function parseMatching(chunk: string, metadata: any) {
  const lines = chunk.split(/\r?\n/);
  let currentQ: any = { 
    ...metadata, 
    id: `q-match-${Math.random().toString(36).substr(2, 9)}`,
    status: 'PUBLISHED',
    questionType: 'MATCH_FOLLOWING',
    matchingData: { leftHeader: "List I", rightHeader: "List II", rows: [] },
    createdAt: serverTimestamp(),
    updatedAt: serverTimestamp()
  };

  let state: 'INTRO' | 'TABLE' | 'INSTR' | 'OPTIONS' | 'ANSWER' | 'EXPL' = 'INTRO';
  let introLines: string[] = [];
  let instructionLines: string[] = [];

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    if (isQuestionStart(line) && line !== lines[0]) break;
    
    // Metadata Terminate
    if (NOISE_PATTERNS.some(p => p.test(trimmed))) {
       if (state === 'EXPL') break;
       continue;
    }

    const opt = detectOptionMarker(line);
    const isAns = ANS_MARKERS.some(m => trimmed.toLowerCase().startsWith(m.toLowerCase()));
    const isExpl = EXPL_MARKERS.some(m => trimmed.toLowerCase().startsWith(m.toLowerCase()));
    const isInstr = INSTR_KEYWORDS.some(k => trimmed.toLowerCase().includes(k.toLowerCase()));

    // Layout Analysis for Header Detection
    const parts = trimmed.split(/\s{3,}/);
    const isHeaderRow = parts.length === 2 && (
      trimmed.toLowerCase().includes('list') || 
      trimmed.toLowerCase().includes('column') || 
      trimmed.toLowerCase().includes('group') ||
      trimmed.toLowerCase().includes('country') ||
      trimmed.toLowerCase().includes('scientist') ||
      trimmed.toLowerCase().includes('river') ||
      trimmed.toLowerCase().includes('author') ||
      trimmed.toLowerCase().includes('freedom fighter')
    );

    if (isHeaderRow && state === 'INTRO') {
       state = 'TABLE';
       currentQ.matchingData.leftHeader = parts[0].trim();
       currentQ.matchingData.rightHeader = parts[1].trim();
       continue;
    } else if (isInstr) {
       state = 'INSTR';
    } else if (opt) {
       state = 'OPTIONS';
    } else if (isAns) {
       state = 'ANSWER';
    } else if (isExpl) {
       state = 'EXPL';
    }

    switch (state) {
      case 'INTRO':
        introLines.push(trimmed);
        break;
      case 'TABLE':
        if (parts.length === 2) {
           currentQ.matchingData.rows.push({ left: parts[0].trim(), right: parts[1].trim() });
        }
        break;
      case 'INSTR':
        instructionLines.push(trimmed);
        break;
      case 'OPTIONS':
        if (opt) currentQ[`option${opt.opt}English`] = opt.text;
        break;
      case 'ANSWER':
        const ansMatch = trimmed.match(/[A-D]/i);
        if (ansMatch) currentQ.correctAnswer = ansMatch[0].toUpperCase();
        break;
      case 'EXPL':
        const clean = trimmed.replace(new RegExp(`^(?:${EXPL_MARKERS.join('|')}|${ANS_MARKERS.join('|')})\\s*[:\\-]?\\s*`, 'i'), '');
        if (clean) {
          if (GURMUKHI_REGEX.test(clean)) currentQ.punjabiExplanation = (currentQ.punjabiExplanation || "") + (currentQ.punjabiExplanation ? '\n' : '') + clean;
          else currentQ.englishExplanation = (currentQ.englishExplanation || "") + (currentQ.englishExplanation ? '\n' : '') + clean;
        }
        break;
    }
  }

  currentQ.englishQuestion = introLines.join('\n');
  if (instructionLines.length > 0) currentQ.englishQuestion += '\n\n' + instructionLines.join('\n');

  return { questions: [currentQ] };
}

function parseDeterministicBilingual(chunk: string, metadata: any) {
  const lines = chunk.split(/\r?\n/);
  let currentQ: any = { 
    ...metadata, 
    id: `q-det-${Math.random().toString(36).substr(2, 9)}`, 
    status: 'PUBLISHED', 
    englishQuestion: "", 
    punjabiQuestion: "",
    createdAt: serverTimestamp(),
    updatedAt: serverTimestamp()
  };
  
  let state: 'QUESTION' | 'OPTIONS' | 'ANSWER' | 'EXPL' = 'QUESTION';

  lines.forEach((line) => {
    const trimmed = line.trim();
    if (!trimmed) return;

    if (isQuestionStart(line) && line !== lines[0]) return;

    const opt = detectOptionMarker(line);
    const isAns = ANS_MARKERS.some(m => trimmed.toLowerCase().startsWith(m.toLowerCase()));
    const isExpl = EXPL_MARKERS.some(m => trimmed.toLowerCase().startsWith(m.toLowerCase()));

    if (isAns) state = 'ANSWER';
    else if (isExpl) state = 'EXPL';
    else if (opt) state = 'OPTIONS';

    switch (state) {
      case 'QUESTION':
        if (GURMUKHI_REGEX.test(trimmed)) currentQ.punjabiQuestion += (currentQ.punjabiQuestion ? '\n' : '') + trimmed;
        else currentQ.englishQuestion += (currentQ.englishQuestion ? '\n' : '') + trimmed;
        break;
      case 'OPTIONS': 
        if (opt) currentQ[`option${opt.opt}English`] = opt.text; 
        break;
      case 'ANSWER':
        const ansMatch = trimmed.match(/[A-D]/i);
        if (ansMatch) currentQ.correctAnswer = ansMatch[0].toUpperCase();
        break;
      case 'EXPL':
        const clean = trimmed.replace(new RegExp(`^(?:${EXPL_MARKERS.join('|')}|${ANS_MARKERS.join('|')})\\s*[:\\-]?\\s*`, 'i'), '');
        if (clean) { 
          if (GURMUKHI_REGEX.test(clean)) currentQ.punjabiExplanation = (currentQ.punjabiExplanation || "") + (currentQ.punjabiExplanation ? '\n' : '') + clean; 
          else currentQ.englishExplanation = (currentQ.englishExplanation || "") + (currentQ.englishExplanation ? '\n' : '') + clean; 
        }
        break;
    }
  });

  return { questions: [currentQ] };
}

export function parseBulkQuestions(rawText: string, metadata: any) {
  if (!rawText || !rawText.trim()) return { questions: [] };
  
  const processedText = preprocessText(rawText);
  const fmt = metadata.parserFormat;

  // Use Continuous Scanning Machine for Assertion/Reason
  if (fmt === 'ASSERTION') {
    return parseAssertionReason(processedText, metadata);
  }

  const chunks = segmentTextByQuestions(processedText);
  const allQuestions: any[] = [];

  chunks.forEach((chunk, index) => {
    let result: any = { questions: [] };

    if (fmt === 'MATCHING') result = parseMatching(chunk, metadata);
    else result = parseDeterministicBilingual(chunk, metadata);

    if (result && result.questions && result.questions.length > 0) {
      allQuestions.push(...result.questions);
    }
  });

  return { questions: allQuestions };
}

export function validateMCQSchema(q: any): { errors: string[], warnings: string[] } {
  const errors: string[] = [];
  const warnings: string[] = [];
  if (!q.englishQuestion && !q.punjabiQuestion) errors.push("Question content missing.");
  const optCount = ['A', 'B', 'C', 'D'].filter(l => (q[`option${l}English`] || "").trim()).length;
  if (optCount < 4) errors.push("Less than four options detected.");
  if (!q.correctAnswer) errors.push("Answer missing.");
  return { errors, warnings };
}