export type ParserFormat = 
  | 'CURRENT_AFFAIRS' 
  | 'BILINGUAL_MCQ' 
  | 'ENGLISH_ONLY' 
  | 'PUNJABI_ONLY' 
  | 'MATHEMATICS' 
  | 'REASONING' 
  | 'DIAGRAM' 
  | 'TABLE' 
  | 'GRAPH' 
  | 'MATCHING' 
  | 'ASSERTION' 
  | 'FILL_BLANK' 
  | 'TRUE_FALSE';

// Hardened Noise Registry - Discard whole lines matching these patterns
const NOISE_PATTERNS = [
  /Advertisement/i, /Advertisements/i, /Sponsored/i, 
  /Download Our App/i, /Install App/i, /Get the App/i,
  /Follow Us/i, /Join Telegram/i, /Telegram/i, 
  /WhatsApp Channel/i, /WhatsApp Group/i, /Facebook/i, 
  /Instagram/i, /Twitter/i, /YouTube/i, /LinkedIn/i,
  /Website/i, /Visit/i, /Click Here/i, /Subscribe/i, 
  /Like/i, /Share/i, /Generated by/i, /Prepared by/i, 
  /Compiled by/i, /Powered by/i, /Source/i, /Copyright/i, 
  /All Rights Reserved/i, /Page/i, /©/i,
  /www\./i, /http/i, /\.co/i, /\.com/i, /\.in/i, /\.net/i, /\.org/i
];

// Termination markers for explanations
const EXPLANATION_END_MARKERS = [
  /^[=\-\*\_]{3,}$/,
  /^(?:Q\d+|Q\.\d+|Q\s+\d+|Question\s*\d+|Question-\d+|QUESTION\s*\d+|QUESTION-\d+|\d+\.|\d+\))(?:[\.\s:]|$)/i,
  /^Page/i,
  /^Download Our App/i,
  /^Copyright/i,
  /www\./i
];

export function preprocessText(text: string): string {
  if (!text) return "";
  
  const lines = text.split(/\r?\n/);
  const cleanLines = lines.filter(line => {
    const trimmed = line.trim();
    if (!trimmed) return true; // Preserve blank lines for block logic
    
    const isNoise = NOISE_PATTERNS.some(pattern => pattern.test(trimmed));
    const hasURL = /https?:\/\/[^\s]+/.test(trimmed) || /www\.[^\s]+/.test(trimmed);
    
    return !isNoise && !hasURL;
  });

  return cleanLines.join('\n').trim();
}

function sanitizeExplanation(text: string): string {
  if (!text) return "";
  const lines = text.split('\n');
  const cleanLines: string[] = [];

  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed && EXPLANATION_END_MARKERS.some(marker => marker.test(trimmed))) break;
    // PRESERVE: Keep original line including internal spaces and intended blank lines
    cleanLines.push(line);
  }

  return cleanLines.join('\n').trim();
}

export function parseBulkQuestions(rawText: string, metadata: any) {
  const cleanedText = preprocessText(rawText);
  if (!cleanedText) return { questions: [] };

  const questions: any[] = [];
  
  const questionStartRegex = /(?:^|\n)\s*(?:Q\d+|Q\.\d+|Q\s+\d+|Question\s*\d+|Question-\d+|QUESTION\s*\d+|QUESTION-\d+|\d+\.|\d+\))(?:[\.\s:]|$)/i;
  const firstIndex = cleanedText.search(questionStartRegex);
  
  if (firstIndex === -1) return { questions: [] };

  const content = cleanedText.substring(firstIndex);
  const blocks = content.split(/(?=\n\s*(?:Q\d+|Q\.\d+|Q\s+\d+|Question\s*\d+|Question-\d+|QUESTION\s*\d+|QUESTION-\d+|\d+\.|\d+\))(?:[\.\s:]|$)|^(?:Q\d+|Q\.\d+|Q\s+\d+|Question\s*\d+|Question-\d+|QUESTION\s*\d+|QUESTION-\d+|\d+\.|\d+\))(?:[\.\s:]|$))/i);

  blocks.forEach(block => {
    const trimmedBlock = block.trim();
    if (!trimmedBlock || trimmedBlock.length < 10) return;

    let q: any = {
      ...metadata,
      id: `staged-${Math.random().toString(36).substr(2, 9)}`,
      status: 'PUBLISHED',
      questionType: 'MCQ',
      correctAnswer: 'A',
      diagram_required: false
    };

    const format: ParserFormat = metadata.parserFormat || 'BILINGUAL_MCQ';

    if (format === 'REASONING') {
       q = parseReasoning(trimmedBlock, q, metadata.secondaryLanguage);
    } else if (format === 'ENGLISH_ONLY' || format === 'MATHEMATICS') {
       q = parseDeterministicEnglish(trimmedBlock, q);
    } else {
       q = parseDeterministicBilingual(trimmedBlock, q, metadata.secondaryLanguage);
    }

    questions.push(q);
  });

  return { questions };
}

/**
 * REASONING PARSER v2.0
 * Strictly multiline, zero math preprocessing, exact source preservation.
 */
function parseReasoning(block: string, q: any, secondaryLang: string) {
  const lines = block.split('\n');
  const punjabiRegex = /[\u0A00-\u0A7F]/;
  const hindiRegex = /[\u0900-\u097F]/;
  const scriptRegex = secondaryLang === 'hindi' ? hindiRegex : punjabiRegex;
  const localKey = secondaryLang === 'hindi' ? 'hindiQuestion' : 'punjabiQuestion';
  const expLocalKey = secondaryLang === 'hindi' ? 'hindiExplanation' : 'punjabiExplanation';

  let state: 'QUESTION' | 'OPTIONS' | 'ANSWER' | 'EXPLANATION' = 'QUESTION';
  
  const englishQLines: string[] = [];
  const localQLines: string[] = [];
  const englishExpLines: string[] = [];
  const localExpLines: string[] = [];
  
  const optMarkers = ['A', 'B', 'C', 'D'];
  const ansMarkers = ["Answer", "Official Key", "Correct Answer", "ਉੱਤਰ", "उत्तर", "ਸਹੀ ਉੱਤਰ"];
  const explMarkers = ["Explanation", "Solution", "ਵਿਆਖਿਆ", "व्याख्या", "Rationale"];

  const qMarkerRegex = /^(?:Q\d+|Question\s*\d+|QUESTION\s*\d+|\d+\.|\d+\))(?:[\.\s:]|$)/i;

  lines.forEach((line, idx) => {
    const trimmedLine = line.trim();
    
    // DETECTION: Transition to Options
    if (trimmedLine.match(/^\(?[A-E][\)\.\s:]/i)) {
      state = 'OPTIONS';
    } 
    // DETECTION: Transition to Answer
    else if (ansMarkers.some(m => trimmedLine.toLowerCase().includes(m.toLowerCase()))) {
      state = 'ANSWER';
    }
    // DETECTION: Transition to Explanation
    else if (explMarkers.some(m => trimmedLine.toLowerCase().includes(m.toLowerCase()))) {
      state = 'EXPLANATION';
    }

    if (state === 'QUESTION') {
      const cleanLine = idx === 0 ? line.replace(qMarkerRegex, '') : line;
      if (cleanLine.trim() || (idx > 0 && lines[idx-1].trim() && idx < lines.length -1 && lines[idx+1].trim())) {
        if (scriptRegex.test(cleanLine)) {
          localQLines.push(cleanLine);
        } else {
          englishQLines.push(cleanLine);
        }
      }
    } else if (state === 'OPTIONS') {
      optMarkers.forEach(opt => {
        const regex = new RegExp(`^\\(?${opt}[\\)\\.\\s:]\\s*(.*)`, 'i');
        const match = line.match(regex);
        if (match) {
          const content = match[1];
          if (scriptRegex.test(content)) {
            const optLocalKey = secondaryLang === 'hindi' ? `option${opt}Hindi` : `option${opt}Punjabi`;
            q[optLocalKey] = content.trim();
          } else {
            q[`option${opt}English`] = content.trim();
          }
        }
      });
    } else if (state === 'ANSWER') {
      const match = trimmedLine.match(/(?:Answer|Official Key|Correct Answer|ਉੱਤਰ|उत्तर|ਸਹੀ ਉੱਤਰ)\s*[:\-]?\s*\(?([A-D])\)?/i);
      if (match) q.correctAnswer = match[1].toUpperCase();
    } else if (state === 'EXPLANATION') {
      const text = line.replace(new RegExp(`^(?:${explMarkers.join('|')})\\s*[:\\-]?\\s*`, 'i'), '');
      if (scriptRegex.test(text)) {
        localExpLines.push(text);
      } else {
        englishExpLines.push(text);
      }
    }
  });

  q.englishQuestion = englishQLines.join('\n').trim();
  q[localKey] = localQLines.join('\n').trim();
  q.englishExplanation = sanitizeExplanation(englishExpLines.join('\n'));
  q[expLocalKey] = sanitizeExplanation(localExpLines.join('\n'));

  return q;
}

function parseDeterministicEnglish(block: string, q: any) {
  const lines = block.split('\n');
  const qMarkerRegex = /^(?:Q\d+|Q\.\d+|Q\s+\d+|Question\s*\d+|Question-\d+|QUESTION\s*\d+|QUESTION-\d+|\d+\.|\d+\))(?:[\.\s:]|$)/i;
  
  let section: 'QUESTION' | 'OPTIONS' | 'ANSWER' | 'EXPLANATION' = 'QUESTION';
  const englishQuestionLines: string[] = [];
  const explanationLines: string[] = [];
  
  const optMarkers = ['A', 'B', 'C', 'D'];
  const ansMarkers = ["Answer", "Official Key", "Correct Answer"];
  const explMarkers = ["Explanation", "Solution", "Rationale"];

  lines.forEach((line, idx) => {
    const trimmedLine = line.trim();
    if (!trimmedLine && section !== 'EXPLANATION' && section !== 'QUESTION') return;
    
    if (trimmedLine.match(/^\(?[A-E][\)\.\s:]/i)) {
      section = 'OPTIONS';
    } else if (ansMarkers.some(m => trimmedLine.toLowerCase().includes(m.toLowerCase()))) {
      section = 'ANSWER';
    } else if (explMarkers.some(m => trimmedLine.toLowerCase().includes(m.toLowerCase()))) {
      section = 'EXPLANATION';
    }

    if (section === 'QUESTION') {
      const text = idx === 0 ? line.replace(qMarkerRegex, '') : line;
      englishQuestionLines.push(text);
    } else if (section === 'OPTIONS') {
      optMarkers.forEach(opt => {
        const regex = new RegExp(`^\\(?${opt}[\\)\\.\\s:]\\s*(.*)`, 'i');
        const match = line.match(regex);
        if (match) q[`option${opt}English`] = match[1].trim(); 
      });
    } else if (section === 'ANSWER') {
      const match = trimmedLine.match(/(?:Answer|Official Key|Correct Answer)\s*[:\-]?\s*\(?([A-D])\)?/i);
      if (match) q.correctAnswer = match[1].toUpperCase();
    } else if (section === 'EXPLANATION') {
      const text = line.replace(new RegExp(`^(?:${explMarkers.join('|')})\\s*[:\\-]?\\s*`, 'i'), '');
      explanationLines.push(text);
    }
  });

  q.englishQuestion = englishQuestionLines.join('\n').trim();
  q.englishExplanation = sanitizeExplanation(explanationLines.join('\n'));

  return q;
}

