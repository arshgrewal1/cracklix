// --- CONSTANT REGISTRY ---
export const QUESTION_SENTINEL_REGEX = /^\s*(?:Q|Question|QUESTION|Q\.)\s*(?:NO\.?\s*)?[-.:]?\s*\d+(?:[\.\s:]|$)/i;

export const NOISE_PATTERNS = [
  /^Page\s*\d+/i,
  /^Generated by/i,
  /^Advertisement/i,
  /Download Our (Android |Mobile |iOS )?App/i,
  /^Download/i,
  /^www\./i,
  /^https?:\/\//i,
  /^Copyright/i,
  /^©/i,
  /@/,
  /^\s*(?:Question|QUESTION|Q\.)\s*$/i,
  /^[-\s\.\*_=|+\/\\]{3,}$/,
  /^\s*[\.·,]{1,}\s*$/
];

export const ANS_MARKERS = ["Answer", "Official Key", "Correct Answer", "ਉੱਤਰ", "उत्तर", "ਸਹੀ ਉੱਤਰ", "Sahi Uttar"];
export const EXPL_MARKERS = ["Explanation", "Solution", "ਵਿਆਖਿਆ", "व्याख्या", "Rationale", "ENGLISH EXPLANATION", "PUNJABI EXPLANATION"];
export const INSTR_KEYWORDS = [
  "Choose the correct matching", 
  "Select the correct answer", 
  "Choose the correct option", 
  "Match correctly", 
  "Pick the correct combination", 
  "ਸਹੀ ਮਿਲਾਨ ਦੀ ਚੋਣ ਕਰੋ", 
  "ਸਹੀ ਉੱਤਰ ਚੁਣੋ",
  "Which option shows the correct matching?",
  "Which of the following is correctly matched?",
  "Identify the correct matching.",
  "Find the correct matching.",
  "Select the correctly matched option."
];
export const ACTUAL_QUESTION_KEYWORDS = /^\s*(Which|What|Who|How|Where|When|Identify|Choose|Find|Observe|Count|Select|During\s?which|How\s?many|How\s?much)\s/i;

const GURMUKHI_REGEX = /[\u0A00-\u0A7F]/;

// --- UTILITIES ---

export function isQuestionStart(line: string): boolean {
  if (!line) return false;
  return QUESTION_SENTINEL_REGEX.test(line.trim());
}

export function preprocessText(text: string): string {
  if (!text) return "";
  const lines = text.split(/\r?\n/);
  const cleanLines = lines.filter(line => {
    const trimmed = line.trim();
    if (!trimmed) return true; 
    const isNoise = NOISE_PATTERNS.some(pattern => pattern.test(trimmed));
    return !isNoise;
  });
  return cleanLines.join('\n').trim();
}

/**
 * @fileOverview Institutional Assertion & Reason Detector v1.0.
 * Classifies questions based on structured markers for logical reasoning.
 */
export function detectAssertionReason(text: string): { questionType: 'ASSERTION_REASON' | 'NORMAL', confidence: number } {
  const assertionRegex = /(?:Assertion|Assertlon|ਕਥਨ)\s*(?:\(A\))?\s*[:\-]/i;
  const reasonRegex = /(?:Reason|Reas0n|ਕਾਰਨ|कारन)\s*(?:\(R\))?\s*[:\-]/i;
  
  const hasA = assertionRegex.test(text);
  const hasR = reasonRegex.test(text);
  
  const hasOptA = /^\s*[\(]?A[\)\.]/m.test(text);
  const hasOptB = /^\s*[\(]?B[\)\.]/m.test(text);
  const hasOptC = /^\s*[\(]?C[\)\.]/m.test(text);
  const hasOptD = /^\s*[\(]?D[\)\.]/m.test(text);
  
  const hasOptions = hasOptA && hasOptB && hasOptC && hasOptD;

  if (hasA && hasR && hasOptions) {
    return { questionType: "ASSERTION_REASON", confidence: 1.0 };
  }
  
  return { questionType: "NORMAL", confidence: 0.0 };
}

export function detectOptionMarker(line: string): { opt: 'A' | 'B' | 'C' | 'D', text: string } | null {
  const trimmed = line.trim();
  const patterns = [
    { regex: /^\s*[\(]?([A])[\)\.]\s*(.*)/i, val: 'A' },
    { regex: /^\s*[\(]?([B])[\)\.]\s*(.*)/i, val: 'B' },
    { regex: /^\s*[\(]?([C])[\)\.]\s*(.*)/i, val: 'C' },
    { regex: /^\s*[\(]?([D])[\)\.]\s*(.*)/i, val: 'D' },
    { regex: /^\s*[\(]?(1)[\)\.]\s*(.*)/, val: 'A' },
    { regex: /^\s*[\(]?(2)[\)\.]\s*(.*)/, val: 'B' },
    { regex: /^\s*[\(]?(3)[\)\.]\s*(.*)/, val: 'C' },
    { regex: /^\s*[\(]?(4)[\)\.]\s*(.*)/, val: 'D' },
    { regex: /^\s*([①❶])\s*(.*)/, val: 'A' },
    { regex: /^\s*([②❷])\s*(.*)/, val: 'B' },
    { regex: /^\s*([③❸])\s*(.*)/, val: 'C' },
    { regex: /^\s*([④❹])\s*(.*)/, val: 'D' }
  ];

  for (const p of patterns) {
    const match = trimmed.match(p.regex);
    if (match) return { opt: p.val as any, text: match[match.length - 1].trim() };
  }
  return null;
}

