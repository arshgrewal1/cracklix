function parseBilingual(block: string, q: any, secondaryLang: string) {
  return parseSimpleBilingual(block, q, secondaryLang);
}

function parseAssertion(block: string, q: any, secondaryLang: string) {
  const assMatch = block.match(/(?:Assertion|ЯеЋЯеЦЯее|ЯцЋЯцЦЯце)\s*\(A\)\s*[:\-]?\s*([\s\S]*?)(?=(?:Reason|ЯеЋЯеЙЯе░Яее|ЯцЋЯцЙЯц░ЯцБ)\s*\(R\)\s*[:\-]?)/i);
  const reasMatch = block.match(/(?:Reason|ЯеЋЯеЙЯе░Яее|ЯцЋЯцЙЯц░ЯцБ)\s*\(R\)\s*[:\-]?\s*([\s\S]*?)(?=\n\s*\(?A[\)\.\s:]+|$)/i);
  if (assMatch && reasMatch) {
     q.englishQuestion = `Assertion (A): ${assMatch[1].trim()}\nReason (R): ${reasMatch[1].trim()}`;
  }
  return parseBilingual(block, q, secondaryLang);
}

function parseMatching(block: string, q: any, secondaryLang: string) {
  const lines = block.split('\n');
  const matchingRows: string[] = [];
  const otherLines: string[] = [];
  const matchingRegex = /^[A-E]\.\s+.*?\s+(?:\d+|[I|V|X]+)\.\s+/i;
  lines.forEach(line => {
    if (matchingRegex.test(line.trim())) matchingRows.push(line.trim());
    else otherLines.push(line);
  });
  if (matchingRows.length > 0) {
    const tableHeader = "| Column I | Column II |\n|---|---|";
    const tableRows = matchingRows.map(row => {
       const parts = row.split(/\s+(?=\d+\.|[I|V|X]+\.)/i);
       return `| ${parts[0]?.trim() || ""} | ${parts[1]?.trim() || ""} |`;
    }).join('\n');
    q.table_data = `${tableHeader}\n${tableRows}`;
  }
  return parseBilingual(otherLines.join('\n'), q, secondaryLang);
}

export function validateMCQSchema(q: any): string[] {
  const errors: string[] = [];
  if (!q.englishQuestion && !q.punjabiQuestion && !q.hindiQuestion) errors.push("Statement node missing.");
  
  const optCount = ['A', 'B', 'C', 'D'].filter(l => q[`option${l}English`] || q[`option${l}Punjabi`] || q[`option${l}Hindi`]).length;
  if (optCount !== 4) errors.push(`Registry Violation: ${optCount} options detected (Exactly 4 required).`);

  const optionStrings = ['A', 'B', 'C', 'D']
    .map(l => (q[`option${l}English`] || "") + (q[`option${l}Punjabi`] || "") + (q[`option${l}Hindi`] || ""))
    .filter(Boolean);
  const uniqueOptions = new Set(optionStrings);
  if (uniqueOptions.size < optionStrings.length) {
    errors.push("Registry Violation: Duplicate options detected.");
  }

  if (!q.correctAnswer) errors.push("Answer key missing.");

  const explMarkers = ["Explanation", "Solution", "ЯехЯе┐ЯеєЯеќЯе┐Яеє", "Rationale"];
  const explanation = (q.englishExplanation || "") + (q.punjabiExplanation || "") + (q.hindiExplanation || "");
  const qMarkerRegex = /(?:^|\n)\s*(?:Q\d+|Q\.\d+|Q\s+\d+|Question\s*\d+|Question-\d+|QUESTION\s*\d+|QUESTION-\d+|\d+\.|\d+\))(?:[\.\s:]|$)/i;
  
  if (qMarkerRegex.test(explanation)) {
     errors.push("Structural Failure: Explanation contains next question marker.");
  }

  const leakMarkers = ["Question", "Option A", "Option B", "Advertisement", "www.", "Page", "Copyright"];
  leakMarkers.forEach(marker => {
    if (explanation.includes(marker)) errors.push(`Integrity Violation: Explanation contains forbidden node "${marker}".`);
  });

  return errors;
}

